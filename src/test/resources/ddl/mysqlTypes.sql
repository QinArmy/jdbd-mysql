    my_decimal_unsigned   decimal(14, 2) UNSIGNED                                             NULL DEFAULT 0.00,
    my_float              float                                                               NULL DEFAULT 0.00,
    my_float_unsigned     float UNSIGNED                                                      NULL DEFAULT 0.00,
    my_double             double                                                              NULL DEFAULT 0.00,
    my_double_unsigned    double UNSIGNED                                                     NULL DEFAULT 0.00,

    my_tiny_text          tinytext                                                            NULL,
    my_text               text                                                                NULL,
