    my_smallint           smallint                                                            NULL DEFAULT 0,
    my_smallint_unsigned  smallint UNSIGNED                                                   NULL DEFAULT 0,
    my_mediumint          mediumint                                                           NULL DEFAULT 0,
    my_mediumint_unsigned mediumint UNSIGNED                                                  NULL DEFAULT 0,
