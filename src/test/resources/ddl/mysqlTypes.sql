    my_medium_blob        mediumblob                                                          NULL,
    my_long_blob          longblob                                                            NULL,

    my_geometry           geometry                                                            NULL,
    my_point              point                                                               NULL,
    my_linestring         LINESTRING                                                          NULL,
    my_polygon            POLYGON                                                             NULL,
    my_multipoint         MULTIPOINT                                                          NULL,
    my_multilinestring    MULTILINESTRING                                                     NULL,
    my_multipolygon       MULTIPOLYGON                                                        NULL,
    my_geometrycollection GEOMETRYCOLLECTION                                                  NULL,

